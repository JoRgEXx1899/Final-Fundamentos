package negocio;

public class Espacio {
    private int K_IdE;
    private int disponible;
    private int K_NumCont;
    private int conse_Reg;

    public int getK_IdE() {
        return K_IdE;
    }

    public void setK_IdE(int K_IdE) {
        this.K_IdE = K_IdE;
    }

    public int getDisponible() {
        return disponible;
    }

    public void setDisponible(int disponible) {
        this.disponible = disponible;
    }

    public int getK_NumCont() {
        return K_NumCont;
    }

    public void setK_NumCont(int K_NumCont) {
        this.K_NumCont = K_NumCont;
    }

    public int getConse_Reg() {
        return conse_Reg;
    }

    public void setConse_Reg(int conse_Reg) {
        this.conse_Reg = conse_Reg;
    }
    
}
