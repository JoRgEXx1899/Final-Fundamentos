package negocio;

public class Vehiculo {
    private String K_Placa;
    private String N_Marca;
    private String N_Modelo;
    private String TipoVeh;

    public String getK_Placa() {
        return K_Placa;
    }

    public void setK_Placa(String K_Placa) {
        this.K_Placa = K_Placa;
    }

    public String getN_Marca() {
        return N_Marca;
    }

    public void setN_Marca(String N_Marca) {
        this.N_Marca = N_Marca;
    }

    public String getN_Modelo() {
        return N_Modelo;
    }

    public void setN_Modelo(String N_Modelo) {
        this.N_Modelo = N_Modelo;
    }

    public String getTipoVeh() {
        return TipoVeh;
    }

    public void setTipoVeh(String TipoVeh) {
        this.TipoVeh = TipoVeh;
    }
    
}
