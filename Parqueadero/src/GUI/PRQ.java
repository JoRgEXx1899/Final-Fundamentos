    private void ComboBoxEntradaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ComboBoxEntradaActionPerformed
        String temp = ComboBoxEntrada.getSelectedItem().toString();
        if (temp == "Contrato") {
            jLabel1.setText("Numero de contrato");
            jLabel2.setText("Fecha de Inicio");
            jLabel3.setText("Fecha Final");
            jLabel4.setText("Valor");
            jLabel5.setText("Cédula del cliente");
            jLabel6.setText("Nombre");
            jLabel7.setText("Teléfono");
            jLabel8.setText("Correo");
            TxtBox2.setText("dd/MM/yyyy");
            TxtBox3.setText("dd/MM/yyyy");
            TxtBox2.setEnabled(true);
            TxtBox6.setVisible(true);
            jLabel6.setVisible(true);
            jLabel7.setVisible(true);
            jLabel8.setVisible(true);

            TxtBox7.setVisible(true);
            TxtBox8.setVisible(true);

        } else if (temp == "Registro unico") {
            jLabel1.setText("Consecutivo");
            jLabel2.setText("Fecha de Inicio");
            jLabel3.setText("Placa");
            jLabel4.setText("Marca");
            jLabel5.setText("Modelo");
            
            TxtBox1.setText("");
            TxtBox2.setText(Integer.toString(c1.get(Calendar.HOUR))+":"+Integer.toString(c1.get(Calendar.MINUTE)));
            TxtBox3.setText("");
            TxtBox2.setEnabled(false);
            TxtBox6.setVisible(false);
            jLabel6.setVisible(false);
            jLabel7.setVisible(false);
            jLabel8.setVisible(false);

            TxtBox7.setVisible(false);
            TxtBox8.setVisible(false);

        }
    }//GEN-LAST:event_ComboBoxEntradaActionPerformed

