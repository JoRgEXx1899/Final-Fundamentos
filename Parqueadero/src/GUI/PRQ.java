    private void ButtonIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonIngresarActionPerformed

        try {

            if (ComboBoxEntrada.getSelectedItem().toString().equals("Contrato")) {
                contrato.setK_NumCont(Integer.parseInt(TxtBox1.getText()));
                contrato.setTipoV(ComboBoxEntrada.getSelectedItem().toString());
                contrato.setF_Inicio(TxtBox2.getText());
                contrato.setF_Final(TxtBox3.getText());
                contrato.setValor(Integer.parseInt(TxtBox4.getText()));
                contrato_dao.setContrato(contrato);
                contrato_dao.incluirContrato();

                cliente.setK_Cc(Integer.parseInt(TxtBox5.getText()));
                cliente.setN_Nombre(TxtBox6.getText());
                cliente.setK_Telefono(TxtBox7.getText());
                cliente.setCorreo(TxtBox8.getText());
                cliente.setK_NumCont(Integer.parseInt(TxtBox1.getText()));
                cliente_dao.setCliente(cliente);
                cliente_dao.incluirCliente();                
            } else {
                registro.setK_CONSECUTIVO(Integer.parseInt(TxtBox1.getText()));
                registro.setF_HEntrada(TxtBox2.getText());
                registro.setF_HSalida(null);
                registro.setTotal(0);
                registro.setK_Placa(TxtBox3.getText());
                registro_dao.setRegistro(registro);
                registro_dao.incluirRegistro();
                
                vehiculo.setK_Placa(TxtBox3.getText());
                vehiculo.setN_Marca(TxtBox4.getText());
                vehiculo.setN_Modelo(TxtBox5.getText());
                vehiculo.setTipoVeh(ComboBoxEntrada.getSelectedItem().toString());
                vh_dao.setVehiculo(vehiculo);
                vh_dao.incluirVehiculo();              
            }
        } catch (CaException ex) {
            Logger.getLogger(PRQ.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_ButtonIngresarActionPerformed

